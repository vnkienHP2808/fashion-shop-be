CREATE TABLE category (
  id_cat BIGINT PRIMARY KEY,
  name_cat VARCHAR(255) NOT NULL,
  status VARCHAR(100),
  image_link VARCHAR(500)
);

CREATE TABLE subcategory (
  id_subcat BIGINT PRIMARY KEY,
  name_subcat VARCHAR(255) NOT NULL,
  id_cat BIGINT NOT NULL,
  FOREIGN KEY (id_cat) REFERENCES category(id_cat)
);

CREATE TABLE product (
  id_product BIGINT PRIMARY KEY,
  name_product VARCHAR(500) NOT NULL,
  id_cat BIGINT NOT NULL,
  id_subcat BIGINT NOT NULL,
  is_new TINYINT(1),
  is_sale TINYINT(1),
  price INT,
  sale_price INT NULL,
  occasion VARCHAR(255),
  sold_quantity INT,
  in_stock INT,
  status VARCHAR(100),
  FOREIGN KEY (id_cat) REFERENCES category(id_cat),
  FOREIGN KEY (id_subcat) REFERENCES subcategory(id_subcat)
);

CREATE TABLE image_product (
  id_image BIGINT PRIMARY KEY,
  image_link VARCHAR(500) NOT NULL,
  id_product BIGINT NOT NULL,
  FOREIGN KEY (id_product) REFERENCES product(id_product)
);

CREATE TABLE user (
  id_user BIGINT AUTO_INCREMENT PRIMARY KEY,
  name VARCHAR(255),
  email VARCHAR(255) NOT NULL UNIQUE,
  password VARCHAR(500) NOT NULL,
  status VARCHAR(100),
  role VARCHAR(100)
);

CREATE TABLE user_phones (
  id_user BIGINT NOT NULL,
  phone VARCHAR(20),
  FOREIGN KEY (id_user) REFERENCES user(id_user) ON DELETE CASCADE,
  PRIMARY KEY (id_user, phone)
);

CREATE TABLE user_addresses (
  id_user BIGINT NOT NULL,
  address VARCHAR(500),
  FOREIGN KEY (id_user) REFERENCES user(id_user) ON DELETE CASCADE,
  PRIMARY KEY (id_user, address)
);

CREATE TABLE cart (
  id_cart BIGINT AUTO_INCREMENT PRIMARY KEY,
  id_user BIGINT NOT NULL UNIQUE,
  created_at DATETIME DEFAULT CURRENT_TIMESTAMP,
  FOREIGN KEY (id_user) REFERENCES user(id_user) ON DELETE CASCADE
);

CREATE TABLE cart_item (
  id_cart_item BIGINT AUTO_INCREMENT PRIMARY KEY,
  id_cart BIGINT NOT NULL,
  id_product BIGINT NOT NULL,
  size VARCHAR(50),
  quantity INT,
  FOREIGN KEY (id_cart) REFERENCES cart(id_cart) ON DELETE CASCADE,
  FOREIGN KEY (id_product) REFERENCES product(id_product)
);

CREATE TABLE `order` (
  id_order BIGINT AUTO_INCREMENT PRIMARY KEY,
  id_user BIGINT NOT NULL,
  status VARCHAR(100),
  order_date DATETIME DEFAULT CURRENT_TIMESTAMP,
  address VARCHAR(500),
  phone_number VARCHAR(20),
  payment_method VARCHAR(100),
  shipping_fee INT,
  grand_total INT,
  FOREIGN KEY (id_user) REFERENCES user(id_user)
);

CREATE TABLE order_detail (
  id_order_detail BIGINT AUTO_INCREMENT PRIMARY KEY,
  id_order BIGINT NOT NULL,
  id_product BIGINT NOT NULL,
  quantity INT,
  size VARCHAR(50),
  total_amount INT,
  FOREIGN KEY (id_order) REFERENCES `order`(id_order) ON DELETE CASCADE,
  FOREIGN KEY (id_product) REFERENCES product(id_product)
);

